            <h1 class="mbr-section-title mbr-fonts-style mbr-regular display-2"> Apoyo a pescadores </h1>
            <!-- <h2 class="mbr-section-subtitle mbr-fonts-style mbr-light display-5">Empresario y Senador por Sonora
        </h2> -->
            <h3 class="mbr-text mbr-fonts-style mbr-regular display-7" style="text-align: justify;">
                29. Del Sen. Arturo Bours Griffith, del Grupo Parlamentario Morena, con punto de acuerdo por el que
                se exhorta a los titulares de la Secretaría de Agricultura y Desarrollo Rural, de la Secretaría de
                Hacienda y Crédito Público, de la Secretaría de Economía y de la Comisión Nacional de Acuacultura y
                Pesca, a efectos de diseñar, de manera inmediata, un programa emergente de apoyo a los pescadores
                del estado de Sonora y a que en el proyecto de Presupuesto de Egresos de la Federación para el
                ejercicio fiscal 2021, sean considerados recursos extraordinarios específicamente para apoyar a los
                pescadores del estado de Sonora. </br></br>


                <center>CONSIDERACIONES:</center> <br>

                Sonora representa el estado con mayor volumen de producción pesquera a
                nivel nacional, 1 de cada 3 toneladas de la producción nacional se pescan
                en esta entidad, sin embargo desde hace varios años, el sector pesquero de
                altura y de ribera atraviesa por malos momentos, registra una disminución en
                las capturas, especialmente en especies como sardina, camarón de bahía,
                calamar gigante y agua mala, entre otras; una alta incidencia de la pesca
                furtiva en los periodos de veda de las especies y una deficiente labor de
                inspección y vigilancia. </br>
                En el año 2018 se lograron pescar en Sonora 7 41 mil 111.76 toneladas que
                tuvieron un valor en el mercado de 7,9 mil millones de pesos, lo que representa un precio promedio de
                11.94 pesos por kilogramo, rendimiento
                mucho menor al de otras entidades federativas del país. <br>

                Es necesario mencionar que los pescadores se encuentran pasando por una
                situación extremadamente complicada, una serie de factores entre los que
                destaca la pandemia de Covid-19 y los efectos perniciosos que ha tenido
                sobre la inmensa mayoría de las actividades económicas, ha puesto a miles
                de familias en situación de alta vulnerabilidad, sin embargo,
                desafortunadamente no ha sido el único factor que les ha afectado. <br>

                Por citar un ejemplo, el puerto de Y avaros, que al igual que los otros puertos
                tienen más de 3 meses sin actividad lo que ha afectado aproximadamente a
                5 mil familias, de donde se obtiene callo de hacha, almeja reina, almeja
                chocolata, lisa y jaiba.</br>
                Las familias dependientes de este puerto señalan que los últimos meses
                apenas han podido sobrevivir a través de créditos en las tiendas de la
                comunidad, malbaratando algunos de sus bienes y pidiendo prestado en
                efectivo, para muchos es la peor crisis que han vivido en los últimos 1O años. <br>
                Posterior a los primeros meses, volvieron a reabrir para aprovechar la zafra
                de lisa, la cual en un buen día puede generar hasta 30 kilos a un precio de
                15 pesos el kilogramo.</br>

                Otra situación que les afecto en las últimas semanas fue el huracán
                "Genevieve" que los mantuvo alejados de sus embarcaciones durante 3 días.
                En la actualidad el comercio ya se reactivó en el puerto, y los pequeños
                restaurantes que lograron sobrevivir, están vendiendo, pero apenas obtienen
                el 30% de los ingresos que solían tener antes de la contingencia sanitaria.</br></br>


                SE REMITIÓ A LA CÁMARA DE DIPUTADOS. </br>
                Fecha de publicación: Miércoles 02 de septiembre de 2020
