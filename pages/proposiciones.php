            <h1 class="mbr-section-title mbr-fonts-style mbr-regular display-2"> Educación fisica </h1>
            <!-- <h2 class="mbr-section-subtitle mbr-fonts-style mbr-light display-5">Empresario y Senador por Sonora
        </h2> -->
            <h3 class="mbr-text mbr-fonts-style mbr-regular display-7" style="text-align: justify;">
                Del Sen. Arturo Bours Griffith, del Grupo Parlamentario Morena, con punto de acuerdo que exhorta a
                las secretarías de Educación Pública; de Salud; y del Trabajo y Previsión Social para que, en
                colaboración con los gobiernos de las 31 entidades federativas y la Ciudad de México, se retome la
                función de los profesores de educación física, con la finalidad de disminuir los niveles de
                obesidad, sobrepeso y diabetes en los niños, niñas y adolescentes. </br></br>

                <center>CONSIDERACIONES:</center> <br>

                En días pasados recibí en mi oficina el documento titulado La Educación Física como Asignatura -cuya
                copia adjunto-, elaborado por la Asociación Nacional Cívica de Profesores y Licenciados en Educación
                Física, A.C. y apoyado en todos sus puntos y propuestas por buena parte de los maestros de educación
                física del Estado de Sonora. </br>

                En dicho documento plantean, entre otras propuestas, lo siguiente:</br>

                “Medie para promover, que la educación física sea incluida en el artículo 3 de la Constitución Política
                de los Estados Unidos Mexicanos, ya que es una actividad educativa que contribuye, ampliamente, en el
                precepto de desarrollar armónicamente a los individuos, así como también se le dé a la educación física
                nuevamente, el carácter de asignatura que hasta el ciclo escolar 2017-2018 ésta tenía dentro de los
                planes y programas de estudio de educación básica”</br>

                “Reconocer en la Ley de Cultura Física y Ley General de Educación al Licenciado de Educación Física como
                un verdadero profesional, ya que actualmente ante la Ley de Servicio Profesional Docente, estamos
                catalogados como técnicos auxiliares en educación, en demérito de la formación académica cursada.”</br>

                Los dos párrafos anteriores me llevaron a pensar, detenidamente, en el papel positivo que debe jugar la
                actividad física desde la más temprana edad en la formación de niños, jóvenes y adolescentes al margen
                de la posición económica de sus familias y del lugar donde viven.</br>

                En consecuencia, después de revisar algunos trabajos relacionados con el papel formativo de la actividad
                física y deportiva en la formación de la persona, comparto con ustedes algunas conclusiones preliminares
                y generales.</br>

                En relación al caso de la obesidad y el sobrepeso, la Organización Mundial de la Salud (OMS), señala que
                más de 1,900 millones de adultos de 18 años o más tenían sobrepeso de los cuales, más de 650 millones
                eran obesos [*] . De igual manera, 41 millones de niños menores de cinco años tenían sobrepeso y, al
                mismo tiempo, eran obesos.</br>

                Lo anterior, obesidad y sobrepeso, incide de manera negativa en el crecimiento económico a la vez que
                contribuye a mantenerlos en la pobreza, condición que afecta de manera más negativa a los niños.</br>

                También, de acuerdo con estudios del Banco Mundial y la Organización Internacional del Trabajo, las
                pérdidas de los países en desarrollo por la malnutrición se encuentran entre el 2% y 3% del PIB, y en lo
                que respecta a los efectos en materia de productividad, la condición de obesidad reduce en un 10% los
                ingresos que una persona obtendría a lo largo de su vida [*] .</br>

                Para el caso de nuestro país, el Instituto Nacional de Salud Pública (INSP) menciona que de los 53
                millones de personas que conforman la Población Económicamente Activa (PEA), 32 millones de ellas
                enfrentan problemas de obesidad y sobrepeso.</br>

                Por otra parte, la Organización Mundial de la Salud (OMS) señala, con una visión amplia del concepto,
                que la salud incluye el acceso oportuno, aceptable y asequible a servicios de atención de calidad
                suficiente [*] .</br>

                La salud constituye un derecho fundamental para la protección y bienestar de la población, además de ser
                uno de los componentes indispensables del capital humano y su formación, que contribuye a garantizar la
                calidad de vida de las personas.</br></br>

                SE TURNÓ A LA COMISIÓN DE EDUCACIÓN. <strong style="float: right; color: green;"> APROBADA </strong> </br>
                Fecha de publicación: Miércoles 10 de abril de 2019
            </h3>
